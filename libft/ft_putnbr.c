#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	else if (n > 9)
	{
		ft_putnbr((size_t)(n / 10));
		ft_putchar(ft_abs(n % 10) + '0');
	}
	else
		ft_putchar(ft_abs(n % 10) + '0');
}
